package ch05;

public class GetterSetterPractice2 {

private String name;
private String race;
private int height;
private int voice;

public String getName() {
	return name;
}
public String getRace() {
	return race;
}
public int getHeight() {
	return height;
}
public int getVoice() {
	return voice;
}
public void setName(String name) {
	this.name = name;
}
public void setRace(String race) {
	this.race = race;
}
public void setHeight(int height) {
	this.height = height;
}
public void setVoice(int voice) {
	this.voice = voice;
} 
}
